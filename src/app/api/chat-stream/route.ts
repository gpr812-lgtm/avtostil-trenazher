  if (!mentioned.includes('—В—А–µ–є–і-–Є–љ')) nextQuestions.push('–Р —В—А–µ–є–і-–Є–љ?');
  if (!mentioned.includes('—Б—А–Њ–Ї–Є')) nextQuestions.push('–Ъ–Њ–≥–і–∞ –Љ–Њ–ґ–љ–Њ –Ј–∞–±—А–∞—В—М?');
