      when_buy: '–Э–∞ —Н—В–Њ–є –љ–µ–і–µ–ї–µ –њ–ї–∞–љ–Є—А—Г—О.',
      how_pay: '–Э–∞–ї–Є—З–љ—Л–Љ–Є —Б–Ї–Њ—А–µ–µ –≤—Б–µ–≥–Њ.',
      phone: '–Ґ–µ–ї–µ—Д–Њ–љ –њ—А–Њ–і–Є–Ї—В—Г—О.',
      city: '–Ш–Ј –Ь–Њ—Б–Ї–≤—Л.',
