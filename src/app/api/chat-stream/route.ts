  if (/—В—А–µ–є–і|–≤ –Ј–∞—З—С—В/.test(allText)) mentioned.push('—В—А–µ–є–і-–Є–љ');
  if (/—Б—А–Њ–Ї[–Є—Г]|–Ї–Њ–≥–і–∞ –Љ–Њ–ґ–љ–Њ|–њ—А–Є–µ—Е–∞—В—М/.test(allText)) mentioned.push('—Б—А–Њ–Ї–Є');
  if (/–Ї–Њ–Љ–њ–ї–µ–Ї—В–∞—Ж–Є/.test(allText)) mentioned.push('–Ї–Њ–Љ–њ–ї–µ–Ї—В–∞—Ж–Є–Є');
  if (/–і–Њ—Б—В–∞–≤–Ї/.test(allText)) mentioned.push('–і–Њ—Б—В–∞–≤–Ї—Г');
  if (/–і–Њ–Ї—Г–Љ–µ–љ—В/.test(allText)) mentioned.push('–і–Њ–Ї—Г–Љ–µ–љ—В—Л');
