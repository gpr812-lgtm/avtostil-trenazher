  // 6.1 вАФ –µ—Б–ї–Є –њ—А–Њ–і–∞–≤–µ—Ж –Ј–∞–і–∞–ї –≤–Њ–њ—А–Њ—Б, –±–Њ—В –Ф–Ю–Ы–Ц–Х–Э –Њ—В–≤–µ—В–Є—В—М
