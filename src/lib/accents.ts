// Контекстные омонимы — слова с ударением, зависящим от контекста
// Обрабатываются ДО основного словаря, чтобы потом не перетирались
function applyContextualHomonyms(text: string): string {
  let result = text;

  // Граница слова для кириллицы (вместо \b, который не работает с кириллицей)
  const wb = '(?<![а-яёА-ЯЁa-zA-Z\\u0300-\\u036F])';
  const wbe = '(?![а-яёА-ЯЁa-zA-Z\\u0300-\\u036F])';

  // «Цена» — зависит от следующего слова
  // «Цена 2 миллиона» → «Це́на» (subject)
  // «Это цена́ хорошая» → «цена́» (сказуемое)
  // Правило: если после «цена» идёт цифра → ударение на «е»
  result = result.replace(
    new RegExp(`${wb}Цена${wbe}\\s+(\\d)`, 'g'),
    'Це́на $1'
  );
  result = result.replace(
    new RegExp(`${wb}цена${wbe}\\s+(\\d)`, 'g'),
    'це́на $1'
  );

  // «уже» — зависит от контекста:
  // «Я уже был там» → «уже́» (previously)
  // «Это уже лучше» → «у́же» (narrower/comparative)
  const alreadyVerbs = 'был|была|было|были|сделал|сделала|сделали|пришел|пришла|пришли|ушел|ушла|ушли|приехал|приехала|приехали|позвонил|позвонила|позвонили|написал|написала|написали|сказал|сказала|сказали|купил|купила|купили|продал|продала|продали|видел|видела|видели|знал|знала|знали|хотел|хотела|хотели|думал|думала|думали|ждал|ждала|ждали|начал|начала|начали|кончил|кончила|кончили|решил|решила|решили|понял|поняла|поняли|спросил|спросила|спросили|ответил|ответила|ответили';
  result = result.replace(
    new RegExp(`${wb}уже${wbe}\\s+(${alreadyVerbs})${wbe}`, 'gi'),
    (match, p1) => match.startsWith('У') ? 'Уже́ ' + p1 : 'уже́ ' + p1
  );

  const comparativeWords = 'лучше|хуже|больше|меньше|быстрее|медленнее|дольше|выше|ниже|ближе|дальше|раньше|позже|красивее|интереснее|проще|сложнее|дешевле|дороже|сильнее|слабее|теплее|холоднее|светлее|темнее|тяжелее|легче|труднее';
  result = result.replace(
    new RegExp(`${wb}уже${wbe}\\s+(${comparativeWords})${wbe}`, 'gi'),
    (match, p1) => match.startsWith('У') ? 'У́же ' + p1 : 'у́же ' + p1
  );

  // «замок» — зависит от значения:
  // «дверной замок» → «за́мок» (lock)
  // «старинный замок» → «замо́к» (castle)
  if (new RegExp(`${wb}(дверн\\w*|ключ\\w*|входн\\w*|сейф\\w*)${wbe}\\s+(?:(?:\\w+\\s+){0,2})?замок${wbe}`, 'i').test(result)) {
    result = result.replace(new RegExp(`${wb}замок${wbe}`, 'gi'), (match) => {
      if (match === 'Замок') return 'За́мок';
      return 'за́мок';
    });
  }
  if (new RegExp(`${wb}(старинн\\w*|каменн\\w*|рыцарск\\w*|корол\\w*|средневеков\\w*|разрушенн\\w*)${wbe}\\s+(?:(?:\\w+\\s+){0,2})?замок${wbe}`, 'i').test(result)) {
    result = result.replace(new RegExp(`${wb}замок${wbe}`, 'gi'), (match) => {
      if (match === 'Замок') return 'Замо́к';
      return 'замо́к';
    });
  }

  // «мука» — зависит от значения:
  // «печь мука» → «мука́» (flour)
  // «душевная мука» → «му́ка» (torment)
  if (new RegExp(`${wb}(душевн\\w*|духовн\\w*|физическ\\w*|смертн\\w*)`, 'i').test(result)) {
    result = result.replace(new RegExp(`${wb}мука${wbe}`, 'gi'), (match) => {
      if (match === 'Мука') return 'Му́ка';
      return 'му́ка';
    });
  } else if (new RegExp(`${wb}(печь|пирог|хлеб|тесто|пшеничн\\w*|ржан\\w*)`, 'i').test(result)) {
    result = result.replace(new RegExp(`${wb}мука${wbe}`, 'gi'), (match) => {
      if (match === 'Мука') return 'Мука́';
      return 'мука́';
    });
  }

  // «плачу» — зависит от значения:
  // «Я плачу деньги» → «плачу́» (pay)
  // «Я плачу от боли» → «пла́чу» (cry)
  if (new RegExp(`${wb}плачу${wbe}\\s+(деньги|за|налог|кредит|счет|квитанцию|налоги)`, 'i').test(result)) {
    result = result.replace(new RegExp(`${wb}плачу${wbe}`, 'gi'), 'плачу́');
  } else if (new RegExp(`${wb}плачу${wbe}\\s+(от|из-за|слезы|горя|радости|обиды)`, 'i').test(result)) {
    result = result.replace(new RegExp(`${wb}плачу${wbe}`, 'gi'), 'пла́чу');
  }

  return result;
}

// Применяет ударения к тексту
export function applyAccents(text: string): string {
  let result = text;

  // 0. Контекстные омонимы — обработка ДО основного словаря
  result = applyContextualHomonyms(result);

  // Заменяем слова на варианты с ударениями
  // Используем границы слов, чтобы не задеть части других слов
  // Важно: U+0301 (combining acute accent) тоже учитываем — иначе 'рубле' совпадёт с 'рубле́й'
  for (const [word, accented] of Object.entries(accentMap)) {
    // Экранируем спецсимволы в слове
    const escapedWord = word.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
    // lookaround для границ слов (работает с кириллицей)
    // U+0301 включаем в границы — это combining accent, который не должен считаться началом нового слова
    const regex = new RegExp(`(?<![а-яёА-ЯЁa-zA-Z\\u0300-\\u036F])${escapedWord}(?![а-яёА-ЯЁa-zA-Z\\u0300-\\u036F])`, 'gi');

    // Сохраняем регистр первой буквы
    result = result.replace(regex, (match) => {
      if (match === word) return accented;
      if (match === word.charAt(0).toUpperCase() + word.slice(1)) {
        return accented.charAt(0).toUpperCase() + accented.slice(1);
      }
      if (match === word.toUpperCase()) {
        return accented.toUpperCase();
      }
      return accented;
    });
  }

  return result;
}
