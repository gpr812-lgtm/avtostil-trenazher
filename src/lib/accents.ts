// Применяет ударения к тексту
export function applyAccents(text: string): string {
  let result = text;

  // Заменяем слова на варианты с ударениями
  // Используем границы слов, чтобы не задеть части других слов
  for (const [word, accented] of Object.entries(accentMap)) {
    // Экранируем спецсимволы в слове
    const escapedWord = word.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
    // \b работает плохо с кириллицей в JS, используем lookaround
    const regex = new RegExp(`(?<![а-яёА-ЯЁa-zA-Z])${escapedWord}(?![а-яёА-ЯЁa-zA-Z])`, 'gi');

    // Сохраняем регистр первой буквы
    result = result.replace(regex, (match) => {
      if (match === word) return accented;
      if (match === word.charAt(0).toUpperCase() + word.slice(1)) {
        return accented.charAt(0).toUpperCase() + accented.slice(1);
      }
      if (match === word.toUpperCase()) {
        return accented.toUpperCase();
      }
      return accented;
    });
  }

  return result;
}
