export function getScenarioById(id: string): Scenario | null {
  return scenarios.find((s) => s.id === id) || null;
}

export function formatOpeningMessage(
  scenario: Scenario,
  selectedCar: { brand: string; model: string; priceFrom: number; priceTo: number } | null
): string {
  let msg = scenario.openingMessage;
  if (selectedCar) {
    const carName = `${selectedCar.brand} ${selectedCar.model}`;
    const priceMln = (selectedCar.priceFrom / 1000000).toFixed(1).replace('.0', '');
    msg = msg.replace(/\{CAR\}/g, carName);
    msg = msg.replace(/\{PRICE\}/g, priceMln);
  } else {
    msg = msg.replace(/\{CAR\}/g, 'ваш автомобиль');
    msg = msg.replace(/\{PRICE\}/g, 'два с половиной');
  }
  return msg;
}
